package Practicec;

public class Datatypes {
	public static void main(String[]args) {
		int var;
		double var1;
		var=100;
		var1=25.56;
		System.out.println("original value of var:" +var);
		System.out.println("original value of var1:" +var1);
// now devide them into 4
		var=var/4;
		var1=var1/4;
		System.out.println("original value of var:" +var);
		System.out.println("original value of var1:" +var1);
	}

}


