package Practicec;

public class Datatype_ex2 {
	public static void main(String[]args) {
		/*
		 * The character ‘X’ is encoded as the number 88, hence when we increment ‘ch’,
		 * we get character number 89, or ‘Y’.
		 */

		char ch;
		ch = 'x';
		System.out.println("ch contains "+ch);
		ch++;
		System.out.println("ch is now "+ch);
		ch=90;
		System.out.println("ch is now "+ch);
		
	}
	

}
