package Advanced_java_Practice;
//Loop without body.
public class forloop_NoBodyloop_ex12 {
	public static void main(String[]args) {
	int i;
	int sum=0;
	for(i=1;i<=5;sum+=i++) {
		System.out.println("sum is"+sum);
/* Two operations are carried 
on, sum = sum + i and
i = i + 1 */
	}
		

}
}
